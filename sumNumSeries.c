#include <stdio.h>
int main() {
    int i = 1,n,sum=0;
    scanf("%d",&n);
    while(i <=n) {
        //printf("%d ", i);
        sum = sum +i;
        sum = 0+1;
        sum = 1+2;
        sum = 3+3;
        sum = 6+4;
        sum = 10+5;
        i = i +1;
    }
    printf(" = %d ", sum);
}
